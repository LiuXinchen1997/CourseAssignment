package com.PTMSystem.bean;

public class Meal {
	private int mealid;
	private String mealname;
	private int mealmoney;
	private String mealtext;
	private int mealhot;
	private int mealpoint;
	
	public int getMealid() {
		return mealid;
	}
	public void setMealid(int mealid) {
		this.mealid = mealid;
	}
	public String getMealname() {
		return mealname;
	}
	public void setMealname(String mealname) {
		this.mealname = mealname;
	}
	public int getMealmoney() {
		return mealmoney;
	}
	public void setMealmoney(int mealmoney) {
		this.mealmoney = mealmoney;
	}
	public String getMealtext() {
		return mealtext;
	}
	public void setMealtext(String mealtext) {
		this.mealtext = mealtext;
	}
	public int getMealhot() {
		return mealhot;
	}
	public void setMealhot(int mealhot) {
		this.mealhot = mealhot;
	}
	public int getMealpoint() {
		return mealpoint;
	}
	public void setMealpoint(int mealpoint) {
		this.mealpoint = mealpoint;
	}
	
}
