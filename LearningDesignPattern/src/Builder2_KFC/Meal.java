package Builder2_KFC;

public class Meal
{
	private String food; // 主食
	private String drink; // 饮料
	
	public void setFood(String food) {
		this.food = food; 
	}

	public void setDrink(String drink) {
		this.drink = drink; 
	}

	public String getFood() {
		return (this.food); 
	}

	public String getDrink() {
		return (this.drink); 
	}
}