];

ep = [
    0.9931 1.2 0.3350
    1.0493 1.16 0.3638
    1.1175 1.16 0.3853
];

eo = [
    0.8276 1 0.2792
    0.8744 0.96 0.3032
    0.9312 0.97 0.3211
];

np = [
    0.9931 1.2 0.3350
    1.0493 1.16 0.3638
    1.1175 1.16 0.3853
];

no = [
    0.8276 1 0.2792
    0.8744 0.96 0.3032
    0.9312 0.97 0.3211
];


up = [
    45.8 18.7 20.6
    75 20.9 21.5
    44.7 19.4 22.9
];

uo = [
    34.6 16 17.4
    60.6 17.6 15.6
    37.6 14.3 16.4
];

cv = zeros(3,3);
cw = zeros(3,3);

tvp = zeros(3,3);
tvo = zeros(3, 3);
for i=1:3
    for j=1:3
        tvp(i,j) = N(i,j) - up(i,j) * qp(i,j);
        tvo(i,j) = N(i,j) - uo(i,j) * qo(i,j);
    end
end


twp = zeros(3,3);
two = zeros(3,3);

y = 1;
for i=1:3
    for j=1:3
      twp(i,j) = y/ (N(i,j) - up(i,j)* qp(i,j) );  
      two(i,j) = y/ (N(i,j) - uo(i,j)* uo(i,j) );
    end
end


% i表示年份，j表示城市
for i=1:3
    for j=1:3
        cv(i,j) = (0.6*1.47*ep(i,j)* tvp(i,j)  +   0.4* 1.47*eo(i,j) *  tvo(i,j)  )   / N(i,j) ;
        cw(i,j) = ( 0.6 * 1.1 * np(i,j) * twp(i,j)  +   0.4 * 1.1 * no(i,j)  * two(i,j)  )   /  (  qp(i,j) + qo(i,j)  );
    end
end
